yfit=function(x,mu,s,a){
a/(2*x*s*gamma(1/a))*exp(-abs((log(x)-mu)/s)^a)
}
yln=function(x,mu,s){
2/(2*x*s*gamma(1/2))*exp(-((log(x)-mu)/s)^2)
}

ylp=function(x,mu,s){
1/(2*x*s*gamma(1))*exp(-abs((log(x)-mu)/s)^1)
}

